use anyhow::{Context, Result};
use std::fmt::Write as _;
use std::path::{Path, PathBuf};
use wt_api::Instance;

pub fn host_block(instance: &Instance) -> Option<String> {
    let endpoint = instance.endpoint.as_ref()?;
    let mut output = String::new();
    writeln!(output, "Host {}", instance.name).unwrap();
    writeln!(output, "  HostName {}", endpoint.host).unwrap();
    writeln!(output, "  User {}", endpoint.user).unwrap();
    writeln!(output, "  Port {}", endpoint.port).unwrap();
    Some(output)
}

pub fn sync(path: &Path, instances: &[Instance]) -> Result<()> {
    if let Some(parent) = path.parent() {
        std::fs::create_dir_all(parent)
            .with_context(|| format!("create SSH config directory {}", parent.display()))?;
    }
    let mut contents = String::from("# Generated by wt. Do not edit.\n\n");
    for instance in instances {
        if let Some(block) = host_block(instance) {
            contents.push_str(&block);
            contents.push('\n');
        }
    }
    let temporary = temporary_path(path);
    std::fs::write(&temporary, contents)
        .with_context(|| format!("write temporary SSH config {}", temporary.display()))?;
    #[cfg(unix)]
    {
        use std::os::unix::fs::PermissionsExt;
        std::fs::set_permissions(&temporary, std::fs::Permissions::from_mode(0o600))?;
    }
    std::fs::rename(&temporary, path)
        .with_context(|| format!("replace SSH config {}", path.display()))?;
    Ok(())
}

pub fn default_ssh_config_path() -> Result<PathBuf> {
    if let Some(path) = std::env::var_os("XDG_CONFIG_HOME") {
        return Ok(PathBuf::from(path).join("wt/ssh_config"));
    }
    let home = std::env::var_os("HOME").context("HOME is not set")?;
    Ok(PathBuf::from(home).join(".config/wt/ssh_config"))
}

fn temporary_path(path: &Path) -> PathBuf {
    let mut value = path.as_os_str().to_owned();
    value.push(format!(".tmp.{}", std::process::id()));
    PathBuf::from(value)
}

#[cfg(test)]
mod tests {
    use super::*;
    use uuid::Uuid;
    use wt_api::{InstanceName, InstanceStatus, SshEndpoint};

    #[test]
    fn renders_host_block() {
        let instance = Instance {
            id: Uuid::nil(),
            name: InstanceName::parse("repo-feature").unwrap(),
            owner: "lucas".to_owned(),
            source: "source".to_owned(),
            git_ref: None,
            status: InstanceStatus::Running,
            endpoint: Some(SshEndpoint {
                user: "ubuntu".to_owned(),
                host: "192.0.2.2".to_owned(),
                port: 22,
            }),
            last_error: None,
        };
        assert_eq!(
            host_block(&instance).unwrap(),
            "Host repo-feature\n  HostName 192.0.2.2\n  User ubuntu\n  Port 22\n"
        );
    }
}
