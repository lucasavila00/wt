use anyhow::{bail, Context, Result};
use std::fs::{self, OpenOptions};
use std::io::Write;
use std::os::unix::fs::{OpenOptionsExt, PermissionsExt};
use std::path::{Path, PathBuf};
use wt_api::{Instance, InstanceStatus};

const BEGIN: &str = "# BEGIN wt managed include";
const END: &str = "# END wt managed include";

pub fn sync(instances: &[Instance]) -> Result<PathBuf> {
    let home = std::env::var_os("HOME").map(PathBuf::from).context("HOME is not set")?;
    let ssh_dir = home.join(".ssh");
    let managed_dir = ssh_dir.join("wt");
    ensure_directory(&ssh_dir)?;
    ensure_directory(&managed_dir)?;
    let config_path = managed_dir.join("config");
    let known_hosts_path = managed_dir.join("known_hosts");

    let running = instances.iter().filter(|instance| {
        instance.status == InstanceStatus::Running && instance.ssh.is_some()
    });
    let mut config = String::from("# Generated by wt sync. Do not edit.\n");
    let mut known_hosts = String::from("# Generated by wt sync. Do not edit.\n");
    for instance in running {
        let ssh = instance.ssh.as_ref().expect("filtered SSH access");
        if ssh.host_keys.is_empty() {
            bail!("instance {} has no SSH host keys", instance.name);
        }
        config.push_str(&format!(
            "\nHost {}\n  HostName {}\n  User {}\n  Port {}\n  HostKeyAlias {}\n  UserKnownHostsFile {}\n  StrictHostKeyChecking yes\n",
            instance.name,
            ssh.host,
            ssh.user,
            ssh.port,
            instance.name,
            ssh_quote(&known_hosts_path),
        ));
        let known_name = if ssh.port == 22 {
            instance.name.to_string()
        } else {
            format!("[{}]:{}", instance.name, ssh.port)
        };
        for key in &ssh.host_keys {
            let mut fields = key.split_whitespace();
            let kind = fields.next().unwrap_or_default();
            let data = fields.next().unwrap_or_default();
            if kind.is_empty() || data.is_empty() {
                bail!("instance {} has an invalid SSH host key", instance.name);
            }
            known_hosts.push_str(&format!("{known_name} {kind} {data}\n"));
        }
    }
    atomic_write(&config_path, config.as_bytes())?;
    atomic_write(&known_hosts_path, known_hosts.as_bytes())?;
    update_main_config(&ssh_dir.join("config"), &config_path)?;
    Ok(config_path)
}

fn ensure_directory(path: &Path) -> Result<()> {
    fs::create_dir_all(path).with_context(|| format!("create {}", path.display()))?;
    fs::set_permissions(path, fs::Permissions::from_mode(0o700))
        .with_context(|| format!("set permissions on {}", path.display()))
}

fn atomic_write(path: &Path, contents: &[u8]) -> Result<()> {
    let temporary = path.with_extension("wt-new");
    let mut file = OpenOptions::new()
        .write(true).create_new(true).mode(0o600)
        .open(&temporary)
        .with_context(|| format!("create {}", temporary.display()))?;
    let result = (|| {
        file.write_all(contents)?;
        file.sync_all()?;
        fs::rename(&temporary, path)?;
        Ok::<_, std::io::Error>(())
    })();
    if result.is_err() { let _ = fs::remove_file(&temporary); }
    result.with_context(|| format!("atomically update {}", path.display()))
}

fn update_main_config(path: &Path, managed: &Path) -> Result<()> {
    let existing = match fs::read_to_string(path) {
        Ok(value) => value,
        Err(error) if error.kind() == std::io::ErrorKind::NotFound => String::new(),
        Err(error) => return Err(error).with_context(|| format!("read {}", path.display())),
    };
    let mut kept = Vec::new();
    let mut in_managed = false;
    for line in existing.lines() {
        if line == BEGIN { in_managed = true; continue; }
        if line == END { in_managed = false; continue; }
        if !in_managed { kept.push(line); }
    }
    if in_managed { bail!("unterminated wt managed Include in {}", path.display()); }
    while kept.last().is_some_and(|line| line.is_empty()) { kept.pop(); }
    let mut updated = kept.join("\n");
    if !updated.is_empty() { updated.push_str("\n\n"); }
    updated.push_str(&format!("{BEGIN}\nInclude {}\n{END}\n", ssh_quote(managed)));
    atomic_write(path, updated.as_bytes())
}

fn ssh_quote(path: &Path) -> String {
    format!("\"{}\"", path.display().to_string().replace('\\', "\\\\").replace('\"', "\\\""))
}

#[cfg(test)]
mod tests {
    use super::*;
    use uuid::Uuid;
    use wt_api::{InstanceName, SshAccess};

    #[test]
    fn sync_preserves_unrelated_config_and_removes_stale_worlds() {
        let temp = tempfile::tempdir().unwrap();
        std::env::set_var("HOME", temp.path());
        fs::create_dir(temp.path().join(".ssh")).unwrap();
        fs::write(temp.path().join(".ssh/config"), "Host personal\n  HostName example.test\n").unwrap();
        let instance = Instance {
            id: Uuid::new_v4(), name: InstanceName::parse("repo-feature").unwrap(),
            owner: "lucas".into(), status: InstanceStatus::Running,
            source: "git://example/repo".into(), git_ref: None,
            guest_ip: Some("192.0.2.2".into()), last_error: None,
            ssh: Some(SshAccess { user: "wt".into(), host: "192.0.2.2".into(), port: 22,
                host_keys: vec!["ssh-ed25519 AAAATEST guest".into()] }),
        };
        sync(&[instance]).unwrap();
        sync(&[]).unwrap();
        let main = fs::read_to_string(temp.path().join(".ssh/config")).unwrap();
        assert!(main.contains("Host personal"));
        assert_eq!(main.matches(BEGIN).count(), 1);
        let managed = fs::read_to_string(temp.path().join(".ssh/wt/config")).unwrap();
        assert!(!managed.contains("repo-feature"));
    }
}
